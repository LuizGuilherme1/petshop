    private void tblPetMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPetMouseClicked
        if(tblPet.getRowCount() > 0){
            int row = this.tblPet.rowAtPoint(evt.getPoint());
            this.id = (Integer) this.tblPet.getModel().getValueAt(row, 0);
            this.txtnome.setText((String) this.tblPet.getModel().getValueAt(row, 1));
            this.txtPeso.setText((String) this.tblPet.getModel().getValueAt(row, 2).toString());
            this.txtAltura.setText((String) this.tblPet.getModel().getValueAt(row, 3).toString());
            this.txtIdade.setText((String) this.tblPet.getModel().getValueAt(row, 4).toString());
            this.txtGenero.setText((String) this.tblPet.getModel().getValueAt(row, 5));
            this.txtEspecie.setText((String) this.tblPet.getModel().getValueAt(row, 6));
            this.txtRaca.setText((String) this.tblPet.getModel().getValueAt(row, 7));
            //
            Proprietario p = (Proprietario) this.tblPet.getModel().getValueAt(row, 8);
            if (p == null) {
                this.cbProprietario.setSelectedItem(null);
            } else {
                this.cbProprietario.setSelectedItem((String) p.getNome());
            }
            this.txtId.setText((String) this.tblPet.getModel().getValueAt(row, 0).toString());
        }
    }//GEN-LAST:event_tblPetMouseClicked

