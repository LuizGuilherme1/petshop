    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        String a = txtvaria.getText();
        PetService servico = new PetService();
        if(a.equals(null) || a.equals("")){
            List<Pet> lista = servico.listar();
            DefaultTableModel modelo = (DefaultTableModel) this.tblPet.getModel();
            modelo.setNumRows(0);
            for(Pet item: lista) {
                Object[] data = {item.getId(), item.getNome(), item.getPeso(), item.getAltura(),
                    item.getIdade(), item.getGenero(), 
                    item.getEspecie(), item.getRaca(), item.getProprietario()};
                modelo.addRow(data);
            }
        }else {
            List<Pet> lista = servico.buscarPorNome(a);
            DefaultTableModel modelo = (DefaultTableModel) this.tblPet.getModel();
            modelo.setNumRows(0);
            for(Pet item: lista) {
                Object[] data = {item.getId(), item.getNome(), item.getPeso(), item.getAltura(),
                    item.getIdade(), item.getGenero(),
                    item.getEspecie(), item.getRaca(), item.getProprietario()};
                modelo.addRow(data);
            }
        }
            
    }//GEN-LAST:event_btnListarActionPerformed

