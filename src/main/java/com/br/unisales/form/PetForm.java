    private void btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveActionPerformed
        PetService servico = new PetService();
        try{
        
            String nome = txtnome.getText();
            Double peso = Double.valueOf(txtPeso.getText());
            Double altura = Double.valueOf(txtAltura.getText());
            Integer idade = Integer.valueOf(txtIdade.getText());
            String genero = txtGenero.getText();
            String especie = txtEspecie.getText();
            String raca = txtRaca.getText();
            
            ProprietarioService se = new ProprietarioService();
            
            Proprietario proprietario = se.buscarPorNome((String)cbProprietario.getSelectedItem());

            if (id == null) {
                servico.salvar(id, nome, peso, altura, idade, genero, especie, raca, proprietario);
            } else {
                if (txtId.getText().equals("") || txtId.getText()==null) {
                    servico.salvar(id, nome, peso, altura, idade, genero, especie, raca , proprietario);
                } else {
                    Integer idtxt = Integer.valueOf(txtId.getText());
                    servico.salvar(idtxt, nome, peso, altura, idade, genero, especie, raca, proprietario);
                }
            }    
        
        }catch(Exception e){
            txtError.setText("Voce precisa prencher todas as lacunas (id opicional)");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btSaveActionPerformed

