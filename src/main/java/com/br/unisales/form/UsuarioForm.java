    private void tblUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuarioMouseClicked
        if(tblUsuario.getRowCount() > 0){
            int row = this.tblUsuario.rowAtPoint(evt.getPoint());
            this.id = (Integer) this.tblUsuario.getModel().getValueAt(row, 0);
            this.txtNome.setText((String) this.tblUsuario.getModel().getValueAt(row, 1));
            this.txtGenero.setText((String) this.tblUsuario.getModel().getValueAt(row, 2));
            this.txtEmail.setText((String) this.tblUsuario.getModel().getValueAt(row, 3));
            this.txtSenha.setText((String) this.tblUsuario.getModel().getValueAt(row, 4));
            this.txtId.setText((String) this.tblUsuario.getModel().getValueAt(row, 0).toString());
        }    
    }//GEN-LAST:event_tblUsuarioMouseClicked

