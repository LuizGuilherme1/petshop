    private void btListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btListarActionPerformed
        String a = txtvar.getText();
        UsuarioService servico = new UsuarioService();
        if(a.equals(null) || a.equals("")){
            List<Usuario> lista = servico.listar();
            DefaultTableModel modelo = (DefaultTableModel) this.tblUsuario.getModel();
            modelo.setNumRows(0);
            for(Usuario item: lista) {
                Object[] data = {item.getId(), item.getNome(), item.getGenero(), item.getEmail(), item.getSenha()};
                modelo.addRow(data);
            }
        }else {
            List<Usuario> lista = servico.buscarPorNome(a);
            DefaultTableModel modelo = (DefaultTableModel) this.tblUsuario.getModel();
            modelo.setNumRows(0);
            for(Usuario item: lista) {
                Object[] data = {item.getId(), item.getNome(), item.getGenero(), item.getEmail(), item.getSenha()};
                modelo.addRow(data);
            }
        }
    }//GEN-LAST:event_btListarActionPerformed

    private void btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveActionPerformed
        UsuarioService servico = new UsuarioService();
        try{
        
            String nome = txtNome.getText();
            String genero = txtGenero.getText();
            String email = txtEmail.getText();
            String senha = txtSenha.getText();

            if (id == null) {
                servico.salvar(id, nome, genero, email, senha);
            } else {
                if (txtId.getText().equals("") || txtId.getText()==null) {
                    servico.salvar(id, nome, genero, email, senha);
                } else {
                    Integer idtxt = Integer.valueOf(txtId.getText());
                    servico.salvar(idtxt, nome, genero, email, senha);
                }
            }    
        
        }catch(Exception e){
            txtError.setText("Voce precisa prencher todas as lacunas (id opicional)");
            e.printStackTrace();
        }                                     
    }//GEN-LAST:event_btSaveActionPerformed

