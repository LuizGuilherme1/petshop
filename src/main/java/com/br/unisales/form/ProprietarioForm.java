    private void btListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btListarActionPerformed
        String a = txtVar.getText();
        ProprietarioService servico = new ProprietarioService();
        if(a.equals(null) || a.equals("")){
            List<Proprietario> lista = servico.listar();
            DefaultTableModel modelo = (DefaultTableModel) this.tbProprietario.getModel();
            modelo.setNumRows(0);
            for(Proprietario item: lista) {
                Object[] data = {item.getId(), item.getNome(), item.getGenero(), item.getCpf(), item.getEmail(), item.getCelular(), item.getOwnpet()};
                modelo.addRow(data);
            }
        }else {
            List<Proprietario> lista = servico.buscarPorNomes(a);
            DefaultTableModel modelo = (DefaultTableModel) this.tbProprietario.getModel();
            modelo.setNumRows(0);
            for(Proprietario item: lista) {
                Object[] data = {item.getId(), item.getNome(), item.getGenero(), item.getCpf(), item.getEmail(), item.getCelular(), item.getOwnpet()};
                modelo.addRow(data);
            }
        }
    }//GEN-LAST:event_btListarActionPerformed

