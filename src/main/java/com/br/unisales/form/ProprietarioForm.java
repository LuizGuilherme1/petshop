    private void btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveActionPerformed
        ProprietarioService servico = new ProprietarioService();
        try{
        
            String nome = txtNome.getText();
            String cpf = txtCpf.getText();
            String genero = txtGenero.getText();
            String email = txtEmail.getText();
            String celular = txtCelular.getText();
            
            PetService pet = new PetService();
            
            if (id == null) {
                servico.salvar(id, nome, cpf, genero, email, celular, null);
            } else {
                if (txtId.getText().equals("") || txtId.getText()==null) {
                    Proprietario proprietario = servico.buscarPorId(id);
                    servico.salvar(id, nome, cpf, genero, email, celular, pet.achar(proprietario));
                } else {
                    Integer idtxt = Integer.valueOf(txtId.getText());
                    Proprietario proprietario = servico.buscarPorId(id);
                    servico.salvar(idtxt, nome, cpf, genero, email, celular, pet.achar(proprietario));
                }
            }    
        
        }catch(Exception e){
            txtError.setText("Voce precisa prencher todas as lacunas (id opicional)");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btSaveActionPerformed

    private void tbProprietarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbProprietarioMouseClicked
        if(tbProprietario.getRowCount() > 0){
            int row = this.tbProprietario.rowAtPoint(evt.getPoint());
            this.id = (Integer) this.tbProprietario.getModel().getValueAt(row, 0);
            this.txtNome.setText((String) this.tbProprietario.getModel().getValueAt(row, 1));
            this.txtCpf.setText((String) this.tbProprietario.getModel().getValueAt(row, 2));
            this.txtGenero.setText((String) this.tbProprietario.getModel().getValueAt(row, 3));
            this.txtEmail.setText((String) this.tbProprietario.getModel().getValueAt(row, 4));
            this.txtCelular.setText((String) this.tbProprietario.getModel().getValueAt(row, 5));
            this.txtId.setText((String) this.tbProprietario.getModel().getValueAt(row, 0).toString());
        }
    }//GEN-LAST:event_tbProprietarioMouseClicked

