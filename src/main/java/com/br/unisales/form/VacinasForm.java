    private void btsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsearchActionPerformed
        String a = txtVar.getText();
        VacinasService service = new VacinasService();
        if(a.equals(null) || a.equals("")){
            List<Vacinas> list = service.listar();
            DefaultTableModel modelo = (DefaultTableModel) this.tblVac.getModel();
            modelo.setNumRows(0);
            for(Vacinas item: list){
                Object[] data = {item.getId(), item.getNome(), item.getDescricao(), item.getAplicacao(), item.getCadastro()};
                modelo.addRow(data);
            }
        }else{
            List<Vacinas> list = service.buscarPorNome(a);
            DefaultTableModel modelo = (DefaultTableModel) this.tblVac.getModel();
            modelo.setNumRows(0);
            for(Vacinas item: list) {
                Object[] data = {item.getId(), item.getNome(), item.getDescricao(), item.getAplicacao(), item.getCadastro()};
                modelo.addRow(data);
            }
        }
    }//GEN-LAST:event_btsearchActionPerformed

    private void tblVacMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVacMouseClicked
        if(tblVac.getRowCount() > 0){
            int row = this.tblVac.rowAtPoint(evt.getPoint());
            this.id = (Integer) this.tblVac.getModel().getValueAt(row, 0);
            this.txtId.setText((String) this.tblVac.getModel().getValueAt(row, 0));
            this.txtNome.setText((String) this.tblVac.getModel().getValueAt(row, 1));
            this.txtDesc.setText((String) this.tblVac.getModel().getValueAt(row, 2));
        }
    }//GEN-LAST:event_tblVacMouseClicked

