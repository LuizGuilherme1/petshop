    private void btsavaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsavaActionPerformed
        VacinasService service = new VacinasService();
        try {
            String name = txtNome.getText();
            String desc = txtDesc.getText();
            Date apl = new Date();
            Date cad = new Date();
            
            if (id == null) {
                service.salvar(id, name, desc, apl, cad);
            }else {
                if (txtId.getText().equals("") || txtId.getText()==null) {
                    service.salvar(id, name, desc, apl, cad);
                }else {
                    Integer idtxt = Integer.valueOf(txtId.getText());
                    service.salvar(idtxt, name, desc, apl, cad);
                }
            }    
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btsavaActionPerformed

