    private void btListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btListarActionPerformed
        String a = txtvar.getText();
        HistoricoService servico = new HistoricoService();
        if(a.equals(null) || a.equals("")){
            List<Historico> lista = servico.listar();
            DefaultTableModel modelo = (DefaultTableModel) this.tblHis.getModel();
            modelo.setNumRows(0);
            for(Historico item: lista) {
                Object[] data = {item.getId(), item.getPeso(), item.getAltura(), item.getCadastro()};
                modelo.addRow(data);
            }
        }
    }//GEN-LAST:event_btListarActionPerformed

    private void btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveActionPerformed
        HistoricoService servico = new HistoricoService();
        try{
        
            Double peso = Double.parseDouble(txtPeso.getText());
            Double altura = Double.parseDouble(txtAltura.getText());
            Date cad = new Date();

            if (id == null) {
                servico.salvar(id, peso, altura, cad);
            } else {
                if (txtId.getText().equals("") || txtId.getText()==null) {
                    servico.salvar(id, peso, altura, cad);
                } else {
                    Integer idtxt = Integer.valueOf(txtId.getText());
                    servico.salvar(idtxt, peso, altura, cad);
                }
            }    
        
        }catch(Exception e){
            e.printStackTrace();
        }                                     
    }//GEN-LAST:event_btSaveActionPerformed

