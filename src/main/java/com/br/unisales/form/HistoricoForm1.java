    private void tblHisMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHisMouseClicked
        if (tblHis.getRowCount() > 0) {
            int row = this.tblHis.rowAtPoint(evt.getPoint());
            this.id = (Integer) this.tblHis.getModel().getValueAt(row, 0);
            this.txtPeso.setText((String) this.tblHis.getModel().getValueAt(row, 1).toString());
            this.txtAltura.setText((String) this.tblHis.getModel().getValueAt(row, 2).toString());
            
            this.txtId.setText((String) this.tblHis.getModel().getValueAt(row, 0).toString());
        }
    }//GEN-LAST:event_tblHisMouseClicked

